package besic;

public class TypeCasting {
public static void main(String[] args) {
	// implicit type casting
	short s= 45;
	int i = s;
	System.out.println(i);
	
	// explicit type casting
	int j =45;
	short ss= (short)j;
	System.out.println(ss);
	
	double d= 4435444.45443d;
	float f = (float)d;
	System.out.println(f);
	
			
	
	
}
}
